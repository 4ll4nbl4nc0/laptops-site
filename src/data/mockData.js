function buildHighlights(useCase) {
  const map = {
    gaming: ['Ideal para gaming', 'RTX dedicada', '144Hz o superior'],
    office: ['Ideal para oficina', 'Batería extendida', 'Diseño ejecutivo'],
    creator: ['Ideal para diseño y edición', 'Pantalla de alta fidelidad', 'Gran capacidad de RAM'],
    business: ['Ideal para empresa', 'Seguridad biométrica', 'Conectividad completa'],
  }

  return map[useCase] || map.business
}

function makeProduct(config, index) {
  const slug = slugify(`${config.brand}-${config.name}`)
  const price = config.price
  const promoPrice = config.promo ? Math.round(price * 0.88) : null
  const stock = config.stock
  const categoryAccent =
    categoryConfig.find((item) => item.id === config.category)?.accent || 'gaming'

  return {
    id: `prod-${index + 1}`,
    slug,
    sku: `AST-${String(index + 101).padStart(4, '0')}`,
    name: config.name,
    shortName: config.name.split(' ').slice(0, 3).join(' '),
    brand: config.brand,
    category: config.category,
    subcategory: config.subcategory,
    price,
    promoPrice,
    rating: config.rating,
    stock,
    minStock: config.minStock,
    featured: config.featured,
    status: 'active',
    condition: config.condition || 'Nuevo',
    tags: config.tags,
    badges: config.badges,
    useCase: config.useCase,
    highlights: buildHighlights(config.useCase),
    description:
      config.description ||
      'Equipo seleccionado para usuarios que valoran potencia, confiabilidad y una experiencia premium en productividad, gaming o creación.',
    technicalSpecs: {
      processor: config.processor,
      ram: config.ram,
      storage: config.storage,
      gpu: config.gpu,
      display: config.display,
      resolution: config.resolution,
      os: config.os,
      connectivity: config.connectivity,
      ports: config.ports,
      battery: config.battery,
      weight: config.weight,
      color: config.color,
      warranty: config.warranty,
    },
    specsSummary: [
      config.processor,
      config.ram,
      config.storage,
      config.gpu,
      config.display,
    ],
    visuals: [
      createProductVisual(config.name, config.category, categoryAccent),
      createProductVisual(`${config.brand} ${config.subcategory}`, config.brand, categoryAccent),
      createProductVisual(config.processor, config.gpu, categoryAccent),
    ],
    reviews: reviewPool,
    views: config.views,
    reservationCount: config.reservationCount,
    createdAt: `2026-0${(index % 6) + 1}-${String((index % 26) + 1).padStart(2, '0')}T11:15:00`,
  }
}

